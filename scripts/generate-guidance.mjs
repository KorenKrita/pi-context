#!/usr/bin/env bun
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const defaultSourcePath = join(repoRoot, "skills", "context-management", "CORE.md");
const defaultOutputPath = join(repoRoot, "src", "generated-guidance.ts");

export function extractGuidanceSection(source, name) {
  const startMarker = `<!-- ACM:${name}:START -->`;
  const endMarker = `<!-- ACM:${name}:END -->`;
  const starts = source.split(startMarker).length - 1;
  const ends = source.split(endMarker).length - 1;
  if (starts !== 1 || ends !== 1) throw new Error(`Expected exactly one ${name} marker pair; found ${starts}/${ends}`);
  const start = source.indexOf(startMarker) + startMarker.length;
  const end = source.indexOf(endMarker, start);
  return source.slice(start, end).trim();
}

export function deriveGuidance(source) {
  const extract = (name) => extractGuidanceSection(source, name);
  return {
    core: extract("CORE"),
    toolDescriptions: {
      checkpoint: extract("TOOL_CHECKPOINT"),
      timeline: extract("TOOL_TIMELINE"),
      travel: extract("TOOL_TRAVEL"),
    },
    guidanceCues: {
      checkpointStart: extract("CUE_CHECKPOINT_START"),
      checkpointDone: extract("CUE_CHECKPOINT_DONE"),
      timelineActive: extract("CUE_TIMELINE_ACTIVE"),
      timelineCheckpoints: extract("CUE_TIMELINE_CHECKPOINTS"),
      timelineSearch: extract("CUE_TIMELINE_SEARCH"),
      timelineTree: extract("CUE_TIMELINE_TREE"),
      travelPhase: extract("CUE_TRAVEL_PHASE"),
      travelTask: extract("CUE_TRAVEL_TASK"),
    },
    recoveryGuidance: {
      nameCollision: extract("RECOVERY_NAME_COLLISION"),
      hostCapability: extract("RECOVERY_HOST_CAPABILITY"),
      rollbackFailed: extract("RECOVERY_ROLLBACK_FAILED"),
      branchRolledBack: extract("RECOVERY_BRANCH_ROLLED_BACK"),
      rollbackSkipped: extract("RECOVERY_ROLLBACK_SKIPPED"),
      refreshPending: extract("RECOVERY_REFRESH_PENDING"),
      restoredHistory: extract("RECOVERY_RESTORED_HISTORY"),
      refreshExhausted: extract("RECOVERY_REFRESH_EXHAUSTED"),
    },
  };
}

export function renderGuidance(source) {
  const sections = deriveGuidance(source);
  return `// Generated by scripts/generate-guidance.mjs from skills/context-management/CORE.md.\n// Do not edit directly.\n\nexport const ACM_CORE_MARKER = "<!-- PI-CONTEXT:ACM-CORE:v1 -->";\n\nexport const ACM_CORE = ${JSON.stringify(sections.core)};\n\nexport const TOOL_DESCRIPTIONS = ${JSON.stringify(sections.toolDescriptions, null, 2)} as const;\n\nexport const GUIDANCE_CUES = ${JSON.stringify(sections.guidanceCues, null, 2)} as const;\n\nexport const RECOVERY_GUIDANCE = ${JSON.stringify(sections.recoveryGuidance, null, 2)} as const;\n`;
}

function argumentPath(flag, fallback) {
  const index = process.argv.indexOf(flag);
  if (index < 0) return fallback;
  const value = process.argv[index + 1];
  if (!value || value.startsWith("--")) throw new Error(`${flag} requires a path`);
  return value;
}

if (import.meta.main) {
  const sourcePath = argumentPath("--source", defaultSourcePath);
  const outputPath = argumentPath("--output", defaultOutputPath);
  const generated = renderGuidance(readFileSync(sourcePath, "utf8"));
  if (process.argv.includes("--check")) {
    const current = readFileSync(outputPath, "utf8");
    if (current !== generated) throw new Error(`Generated guidance is stale: ${outputPath}`);
    process.stdout.write(`Verified ${outputPath}\n`);
  } else {
    writeFileSync(outputPath, generated);
    process.stdout.write(`Generated ${outputPath}\n`);
  }
}
