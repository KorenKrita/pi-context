#!/usr/bin/env bun
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const defaultCoreSourcePath = join(repoRoot, "skills", "context-management", "CORE.md");
const defaultToolContractsPath = join(repoRoot, "skills", "context-management", "TOOL-CONTRACTS.md");
const defaultOutputPath = join(repoRoot, "src", "generated-guidance.ts");

function normalizeLineEndings(value) {
  return value.replace(/\r\n?/g, "\n");
}

export function extractGuidanceSection(source, name) {
  const startMarker = `<!-- ACM:${name}:START -->`;
  const endMarker = `<!-- ACM:${name}:END -->`;
  const starts = source.split(startMarker).length - 1;
  const ends = source.split(endMarker).length - 1;
  if (starts !== 1 || ends !== 1) throw new Error(`Expected exactly one ${name} marker pair; found ${starts}/${ends}`);
  const start = source.indexOf(startMarker) + startMarker.length;
  const end = source.indexOf(endMarker, start);
  if (end < 0) throw new Error(`Marker ${name}: END must appear after START`);
  return source.slice(start, end).trim();
}

export function deriveGuidance(coreSource, toolContractsSource) {
  const normalizedCoreSource = normalizeLineEndings(coreSource);
  const normalizedToolContractsSource = normalizeLineEndings(toolContractsSource);
  const extractCore = (name) => extractGuidanceSection(normalizedCoreSource, name);
  const extractContract = (name) => extractGuidanceSection(normalizedToolContractsSource, name);
  return {
    core: extractCore("CORE"),
    toolDescriptions: {
      checkpoint: extractContract("TOOL_CHECKPOINT"),
      timeline: extractContract("TOOL_TIMELINE"),
      travel: extractContract("TOOL_TRAVEL"),
    },
    promptSnippets: {
      checkpoint: extractContract("SNIPPET_CHECKPOINT"),
      timeline: extractContract("SNIPPET_TIMELINE"),
      travel: extractContract("SNIPPET_TRAVEL"),
    },
    promptGuidelines: {
      checkpoint: extractContract("GUIDELINE_CHECKPOINT"),
      timeline: extractContract("GUIDELINE_TIMELINE"),
      travel: extractContract("GUIDELINE_TRAVEL"),
    },
    guidanceCues: {
      checkpoint: extractContract("CUE_CHECKPOINT"),
      rebaseCheck: extractContract("CUE_REBASE_CHECK"),
      timelineActive: extractContract("CUE_TIMELINE_ACTIVE"),
      timelineCheckpoints: extractContract("CUE_TIMELINE_CHECKPOINTS"),
      timelineSearch: extractContract("CUE_TIMELINE_SEARCH"),
      timelineTree: extractContract("CUE_TIMELINE_TREE"),
      travel: extractContract("CUE_TRAVEL"),
    },
    recoveryGuidance: {
      nameCollision: extractContract("RECOVERY_NAME_COLLISION"),
      hostCapability: extractContract("RECOVERY_HOST_CAPABILITY"),
      rollbackFailed: extractContract("RECOVERY_ROLLBACK_FAILED"),
      branchRolledBack: extractContract("RECOVERY_BRANCH_ROLLED_BACK"),
      rollbackSkipped: extractContract("RECOVERY_ROLLBACK_SKIPPED"),
      refreshPending: extractContract("RECOVERY_REFRESH_PENDING"),
      restoredHistory: extractContract("RECOVERY_RESTORED_HISTORY"),
      refreshExhausted: extractContract("RECOVERY_REFRESH_EXHAUSTED"),
    },
  };
}

export function renderGuidance(coreSource, toolContractsSource) {
  const sections = deriveGuidance(coreSource, toolContractsSource);
  return `// Generated by scripts/generate-guidance.mjs from skills/context-management/CORE.md and TOOL-CONTRACTS.md.\n// Do not edit directly.\n\nexport const ACM_CORE_MARKER = "<!-- PI-CONTEXT:ACM-CORE:v1 -->";\n\nexport const ACM_CORE = ${JSON.stringify(sections.core)};\n\nexport const TOOL_DESCRIPTIONS = ${JSON.stringify(sections.toolDescriptions, null, 2)} as const;\n\nexport const PROMPT_SNIPPETS = ${JSON.stringify(sections.promptSnippets, null, 2)} as const;\n\nexport const PROMPT_GUIDELINES = ${JSON.stringify(sections.promptGuidelines, null, 2)} as const;\n\nexport const GUIDANCE_CUES = ${JSON.stringify(sections.guidanceCues, null, 2)} as const;\n\nexport const RECOVERY_GUIDANCE = ${JSON.stringify(sections.recoveryGuidance, null, 2)} as const;\n`;
}

function argumentPath(flag, fallback) {
  const index = process.argv.indexOf(flag);
  if (index < 0) return fallback;
  const value = process.argv[index + 1];
  if (!value || value.startsWith("--")) throw new Error(`${flag} requires a path`);
  return value;
}

if (import.meta.main) {
  const legacySourcePath = argumentPath("--source", defaultCoreSourcePath);
  const coreSourcePath = argumentPath("--core-source", legacySourcePath);
  const toolContractsPath = argumentPath("--contracts-source", defaultToolContractsPath);
  const outputPath = argumentPath("--output", defaultOutputPath);
  const generated = renderGuidance(
    readFileSync(coreSourcePath, "utf8"),
    readFileSync(toolContractsPath, "utf8"),
  );
  if (process.argv.includes("--check")) {
    const current = normalizeLineEndings(readFileSync(outputPath, "utf8"));
    if (current !== generated) throw new Error(`Generated guidance is stale: ${outputPath}`);
    process.stdout.write(`Verified ${outputPath}\n`);
  } else {
    writeFileSync(outputPath, generated);
    process.stdout.write(`Generated ${outputPath}\n`);
  }
}
