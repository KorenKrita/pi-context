#!/usr/bin/env bun
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const defaultCorePath = join(repoRoot, "skills", "context-management", "CORE.md");
const defaultContractsPath = join(repoRoot, "skills", "context-management", "TOOL-CONTRACTS.md");
const defaultOutputPath = join(repoRoot, "src", "generated-guidance.ts");

function normalizeLineEndings(value) {
  return value.replace(/\r\n?/g, "\n");
}

export function extractGuidanceSection(source, name) {
  const startMarker = `<!-- ACM:${name}:START -->`;
  const endMarker = `<!-- ACM:${name}:END -->`;
  const starts = source.split(startMarker).length - 1;
  const ends = source.split(endMarker).length - 1;
  if (starts !== 1 || ends !== 1) throw new Error(`Expected exactly one ${name} marker pair; found ${starts}/${ends}`);
  const start = source.indexOf(startMarker) + startMarker.length;
  const end = source.indexOf(endMarker, start);
  if (end < 0) throw new Error(`Marker ${name}: END must appear after START`);
  return source.slice(start, end).trim();
}

export function deriveGuidance(coreSource, contractsSource) {
  const core = extractGuidanceSection(normalizeLineEndings(coreSource), "CORE");
  const contracts = normalizeLineEndings(contractsSource);
  const extract = (name) => extractGuidanceSection(contracts, name);
  return {
    core,
    toolDescriptions: {
      checkpoint: extract("TOOL_CHECKPOINT"),
      timeline: extract("TOOL_TIMELINE"),
      travel: extract("TOOL_TRAVEL"),
    },
    promptSnippets: {
      checkpoint: extract("SNIPPET_CHECKPOINT"),
      timeline: extract("SNIPPET_TIMELINE"),
      travel: extract("SNIPPET_TRAVEL"),
    },
    promptGuidelines: {
      checkpoint: extract("GUIDELINE_CHECKPOINT"),
      timeline: extract("GUIDELINE_TIMELINE"),
      travel: extract("GUIDELINE_TRAVEL"),
    },
    guidanceCues: {
      checkpoint: extract("CUE_CHECKPOINT"),
      travel: extract("CUE_TRAVEL"),
      rebaseCheck: extract("CUE_REBASE_CHECK"),
      advancedTargetPointer: extract("CUE_ADVANCED_TARGET_POINTER"),
      timelineActive: extract("CUE_TIMELINE_ACTIVE"),
      timelineCheckpoints: extract("CUE_TIMELINE_CHECKPOINTS"),
      timelineSearch: extract("CUE_TIMELINE_SEARCH"),
      timelineTree: extract("CUE_TIMELINE_TREE"),
    },
    treeSummaryInstructions: extract("TREE_SUMMARY_INSTRUCTIONS"),
    recoveryGuidance: {
      nameCollision: extract("RECOVERY_NAME_COLLISION"),
      hostCapability: extract("RECOVERY_HOST_CAPABILITY"),
      rollbackFailed: extract("RECOVERY_ROLLBACK_FAILED"),
      branchRolledBack: extract("RECOVERY_BRANCH_ROLLED_BACK"),
      rollbackSkipped: extract("RECOVERY_ROLLBACK_SKIPPED"),
      refreshPending: extract("RECOVERY_REFRESH_PENDING"),
      restoredHistory: extract("RECOVERY_RESTORED_HISTORY"),
      refreshExhausted: extract("RECOVERY_REFRESH_EXHAUSTED"),
    },
  };
}

export function renderGuidance(coreSource, contractsSource) {
  const sections = deriveGuidance(coreSource, contractsSource);
  return `// Generated by scripts/generate-guidance.mjs from skills/context-management/CORE.md and TOOL-CONTRACTS.md.\n// Do not edit directly.\n\nexport const ACM_CORE_MARKER = "<!-- PI-CONTEXT:ACM-CORE:v1 -->";\n\nexport const ACM_CORE = ${JSON.stringify(sections.core)};\n\nexport const TOOL_DESCRIPTIONS = ${JSON.stringify(sections.toolDescriptions, null, 2)} as const;\n\nexport const PROMPT_SNIPPETS = ${JSON.stringify(sections.promptSnippets, null, 2)} as const;\n\nexport const PROMPT_GUIDELINES = ${JSON.stringify(sections.promptGuidelines, null, 2)} as const;\n\nexport const GUIDANCE_CUES = ${JSON.stringify(sections.guidanceCues, null, 2)} as const;\n\nexport const TREE_SUMMARY_INSTRUCTIONS = ${JSON.stringify(sections.treeSummaryInstructions)};\n\nexport const RECOVERY_GUIDANCE = ${JSON.stringify(sections.recoveryGuidance, null, 2)} as const;\n`;
}

function argumentPath(flag, fallback) {
  const index = process.argv.indexOf(flag);
  if (index < 0) return fallback;
  const value = process.argv[index + 1];
  if (!value || value.startsWith("--")) throw new Error(`${flag} requires a path`);
  return value;
}

if (import.meta.main) {
  const corePath = argumentPath("--core", defaultCorePath);
  const contractsPath = argumentPath("--contracts", defaultContractsPath);
  const outputPath = argumentPath("--output", defaultOutputPath);
  const generated = renderGuidance(readFileSync(corePath, "utf8"), readFileSync(contractsPath, "utf8"));
  if (process.argv.includes("--check")) {
    const current = normalizeLineEndings(readFileSync(outputPath, "utf8"));
    if (current !== generated) throw new Error(`Generated guidance is stale: ${outputPath}`);
    process.stdout.write(`Verified ${outputPath}\n`);
  } else {
    writeFileSync(outputPath, generated);
    process.stdout.write(`Generated ${outputPath}\n`);
  }
}
